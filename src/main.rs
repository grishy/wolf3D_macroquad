const SIZE_BOCK: f32 = 32.0;
const PLAYER_SPEED: f32 = 2.0;

struct State {
    player_x: f32,
    player_y: f32,
    player_angle_radians: f32,
}

fn window_conf() -> Conf {
    Conf {
        window_title: "Wolf3D Macroquad".to_owned(),
        window_width: 1024,
        window_height: 1024,
        window_resizable: false,
        ..Default::default()
    }
}

#[macroquad::main(window_conf)]
async fn main() {
    let mut game_state: State = State {
        player_x: 200.0,
        player_y: 200.0,
        player_angle_radians: 0.0,
    };

    loop {
        clear_background(macroquad::color::Color::from_rgba(224, 248, 207, 255));

        for (x, x_line) in MAP.iter().enumerate() {
            for (y, val) in x_line.iter().enumerate() {
                match val {
                    1 => {
                        let start_x = x as f32 * SIZE_BOCK;
                        let start_y = y as f32 * SIZE_BOCK;

                        draw_rectangle(
                            start_x,
                            start_y,
                            SIZE_BOCK,
                            SIZE_BOCK,
                            macroquad::color::Color::from_rgba(48, 104, 80, 255),
                        );
                    }
                    0 => {}
                    _ => panic!("wrong number in map"),
                }
            }
        }

        {
            let length = 64.0;
            let dx = length * game_state.player_angle_radians.cos();
            let dy = length * game_state.player_angle_radians.sin();

            draw_line(
                game_state.player_x,
                game_state.player_y,
                game_state.player_x + dx,
                game_state.player_y + dy,
                2.0,
                BLUE,
            );
        }

        draw_circle(game_state.player_x, game_state.player_y, 15.0, YELLOW);

        if is_key_down(KeyCode::Down) {
            game_state.player_y -= PLAYER_SPEED * game_state.player_angle_radians.sin();
            game_state.player_x -= PLAYER_SPEED * game_state.player_angle_radians.cos();
        }
        if is_key_down(KeyCode::Up) {
            game_state.player_y += PLAYER_SPEED * game_state.player_angle_radians.sin();
            game_state.player_x += PLAYER_SPEED * game_state.player_angle_radians.cos();

        }
        if is_key_down(KeyCode::Right) {
            game_state.player_angle_radians += 0.1;
        }
        if is_key_down(KeyCode::Left) {
            game_state.player_angle_radians -= 0.1;
        }
        if is_key_down(KeyCode::Escape) {
            std::process::exit(0)
        }

        next_frame().await
    }
}
